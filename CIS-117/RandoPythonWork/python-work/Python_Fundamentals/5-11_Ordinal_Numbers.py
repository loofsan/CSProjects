numbers = list(range(1, 10))
for a in numbers:
	if a == 1:
		print(f"{a}, 1st")
	elif a == 2:
		print(f"{a}, 2nd")
	elif a == 3:
		print(f"{a}, 3rd")
	elif a == 4:
		print(f"{a}, 4th")
	elif a == 5:
		print(f"{a}, 5th")
	elif a == 6:
		print(f"{a}, 6th")
	elif a == 7:
		print(f"{a}, 7th")
	elif a == 8:
		print(f"{a}, 8th")
	else:
		print(f"{a}, 9th")
