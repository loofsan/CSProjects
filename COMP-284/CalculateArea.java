/* 
This program calculates the area of a rectangle 
given the length and the width.
*/
public class CalculateArea 
{
    public static void main (String [] args)
    {
        int rectangleLength = 7;
        int rectangleWidth = 9;
        int rectangleArea = rectangleLength * rectangleWidth;
        System.out.println ("A rectangle has a length of " + rectangleLength
                             + " ft. and a width of " + rectangleWidth
                             + " ft. Its area is " + rectangleArea + " sq. ft.");                  
     }
}